    '    dlog_frontier_roll = (\n'
    '        dlog_frontier_raw.T.rolling(deriv_rolling_window, min_periods=deriv_rolling_min)\n'
    '        .mean().T.fillna(0.0)\n'
    '    )\n'
    '    wide_dlog_frontier = dlog_frontier_roll.copy()\n'
    '    wide_dlog_frontier.columns = year_cols\n'
    '\n'
    '    if (~any_activity).any():\n'
    '        wide_dlog_frontier = pd.concat([\n'
    '            wide_dlog_frontier,\n'
    '            pd.DataFrame(0.0, index=list(~any_activity), columns=year_cols),\n'
    '        ]).loc[wide_dlog_frontier.index]\n'
    '\n'
    '    return {\n'
    '        "wide_log_share": wide_log_share,\n'
    '        "wide_log_frontier": wide_log_frontier,\n'
    '        "wide_dlog_share": wide_dlog_share,\n'
    '        "wide_dlog_frontier": wide_dlog_frontier,\n'
    '        "has_activity_mask": any_activity,\n'
    '    }\n'
    '\n'
    '\n'
    '# ---- 计算四套分解轨迹 ----\n'
    'decomp = build_log_decomposition_trajectories(\n'
    '    panel_main, years_all,\n'
    '    DECOMP_LOG_ROLLING_WINDOW, DECOMP_LOG_ROLLING_MIN,\n'
    '    DECOMP_DERIV_ROLLING_WINDOW, DECOMP_DERIV_ROLLING_MIN,\n'
    '    mask=wide_roll_raw.index,\n'
    ')\n'
    '\n'
    'wide_log_share = decomp["wide_log_share"]\n'
    'wide_log_frontier = decomp["wide_log_frontier"]\n'
    'wide_dlog_share = decomp["wide_dlog_share"]\n'
    'wide_dlog_frontier = decomp["wide_dlog_frontier"]\n'
    'any_activity = decomp["has_activity_mask"]\n'
    '\n'
    'print("wide_log_share shape:", wide_log_share.shape)\n'
    'print("wide_log_frontier shape:", wide_log_frontier.shape)\n'
    'print("wide_dlog_share shape:", wide_dlog_share.shape)\n'
