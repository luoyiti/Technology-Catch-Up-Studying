    "**数值稳定**：对 `log(share)` 中 `share=0` 的单元格填充 `1e-10`，使对数有定义且不产生 `-inf`。\n"
    "导数轨迹通过 `[log(t) - log(t-1)]` 计算，再 rolling 平滑以压制单年噪声。"
