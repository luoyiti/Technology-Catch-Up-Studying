    '            plot_embedding(\n'
    '                arr, labels,\n'
    '                out_dir / f"trajectory_{name}_hier_{suffix}.png",\n'
    '                f"Trajectory {name.upper()} (k={k_final}, {suffix})",\n'
    '            )\n'
    '\n'
    '    print(f"Cluster sizes [{suffix}]:\\n", pd.Series(labels).value_counts().sort_index())\n'
    '    return labels, k_final, search_df\n'
    '\n'
    '\n'
    '# =============================================================================\n'
    '# 聚类A: log_share 轨迹\n'
    '# =============================================================================\n'
    'print("\\n" + "=" * 60)\n'
    'print("CLUSTERING A: log_share (country share growth pattern)")\n'
